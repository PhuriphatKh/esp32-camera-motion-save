static esp_err_t init_camera(void)
{
    //initialize the camera
    esp_err_t err = esp_camera_init(&camera_config);
    if (err != ESP_OK)
    {
        ESP_LOGE(TAG, "Camera Init Failed");
        return err;
    }

    return ESP_OK;
}
#endif

static esp_err_t initi_sd_card(void)
{  
    sdmmc_host_t host = SDMMC_HOST_DEFAULT();
    sdmmc_slot_config_t slot_config = SDMMC_SLOT_CONFIG_DEFAULT();
    esp_vfs_fat_sdmmc_mount_config_t mount_config = {
        .format_if_mount_failed = false,
        .max_files = 3,
    };
    sdmmc_card_t *card;
    esp_err_t err = esp_vfs_fat_sdmmc_mount("/sdcard", &host, &slot_config, &mount_config, &card);
    if (err != ESP_OK)
    {
        return err;
    }
    return ESP_OK;
}

static void take_photo(void *arg)
{
    printf("Starting Taking Picture!\n");

    camera_fb_t *pic = esp_camera_fb_get();

    char photo_name[50];
    sprintf(photo_name, "/sdcard/pic_%lli.jpg", pic->timestamp.tv_sec);

    FILE *file = fopen(photo_name, "wb");
    if (file == NULL)
    {
        printf("err: fopen failed\n");
    }
    else
    {
        fwrite(pic->buf, 1, pic->len, file);
        fclose(file);
    }

    printf("Finished Taking Picture!\n");
    
    esp_camera_fb_return(pic);
    vTaskDelete(NULL);
}

void app_main(void)
{
    esp_err_t err;

    #if ESP_CAMERA_SUPPORTED
            if(ESP_OK != init_camera()) {
                return;
            }
    #else
            ESP_LOGE(TAG, "Camera support is not available for this chip");
            return;
    #endif

    // Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND)
    {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    // Initialize the SD card
    ESP_LOGI(TAG, "Initializing SD card");

    err = initi_sd_card();
    if (err != ESP_OK)
    {
        printf("err: %s\n", esp_err_to_name(err));
        return;
    }
    
    gpio_set_direction(GPIO_NUM_13, GPIO_MODE_INPUT);

    while (1)
    {
        if (gpio_get_level(GPIO_NUM_13) == 1)
        {
            printf("Someone is there\n");
            xTaskCreate(take_photo, "pic", configMINIMAL_STACK_SIZE * 5, NULL, 5, NULL);
            vTaskDelay(5000/portTICK_RATE_MS);
        } 
        else 
        {
            printf("no detection\n");
            vTaskDelay(2000/portTICK_PERIOD_MS);
        }
    }
};